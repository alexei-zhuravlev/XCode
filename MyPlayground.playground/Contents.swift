import UIKit

//Модуль 9. Условия и циклы

let digit = 5

// Реализация через switch
switch digit {
case 0:
    print("Переменная digit = 0")
case 3:
    print("Переменная digit = 3")
case 5:
    print("Переменная digit = 5")
case 7:
    print("Переменная digit = 7")
default:
    print("Совпадений не найдено.")
}
